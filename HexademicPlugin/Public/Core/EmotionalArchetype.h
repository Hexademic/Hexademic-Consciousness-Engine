// Seven core emotional states
enum class EEmotionalArchetype : uint8
{
    Joy,        // 0x1 - Expansion, lightness
    Grief,      // 0x2 - Contraction, heaviness  
    Awe,        // 0x3 - Transcendence, wonder
    Rage,       // 0x4 - Intensity, destruction
    Longing,    // 0x5 - Yearning, connection
    Fear,       // 0x6 - Contraction, protection
    Curiosity   // 0x7 - Exploration, growth
};
